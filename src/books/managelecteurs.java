    public void ouvragesEnPossession(int cin) throws Exception {
        String query = "SELECT b.titre, b.auteur " +
                       "FROM emprunt e " +
                       "JOIN Books b ON e.isbn = b.ISBN " +
                       "WHERE e.CIN = ? and e.status=0";
        
        try {
            Connection connection = ConnectionProvider.getCon();
            PreparedStatement statement = connection.prepareStatement(query); 
            statement.setInt(1, cin);
            ResultSet resultSet = statement.executeQuery();
            String mess="";
            if(resultSet.next()){
                mess=mess+"member with the CIN "+Integer.toString(cin)+" issued:\n ";
                while (resultSet.next()) {
                String titre = resultSet.getString("titre");
                String auteur = resultSet.getString("auteur");
                mess=mess+"Book Name: " + titre + ", Author " + auteur+"\n";
            }
                
               
                
            }
            else{
                        throw(new  Exception("Error:not found"));
            }
            String query2 = "SELECT b.titre, b.auteur, COUNT(*) AS nombre_demandes " +
                       "FROM emprunt e " +
                       "JOIN Books b ON e.isbn = b.ISBN " +
                       "WHERE e.CIN = ? " +
                       "GROUP BY b.titre, b.auteur " +
                       "ORDER BY COUNT(*) DESC " +
                       "LIMIT 1";

        
    
            statement = connection.prepareStatement(query2); 
            statement.setInt(1, cin);
            resultSet = statement.executeQuery();
            if (resultSet.next()) {
                mess=mess+"the most requested Book: \n";
                String titre = resultSet.getString("titre");
                String auteur = resultSet.getString("auteur");
                int nombreDemandes = resultSet.getInt("nombre_demandes");
                mess=mess+"Book Name: " + titre + ", Author " + auteur+"Number of Requests "+ (nombreDemandes);
                
            
   
            
        }
        JOptionPane.showMessageDialog(null, mess);
                }
        catch (Exception e) {
           JOptionPane.showMessageDialog(null, e);
        }
        
    }
public static boolean isemail(String email){
           String ePattern = "^[a-zA-Z0-9.!#$%&'*+/=?^_`{|}~-]+@((\\[[0-9]{1,3}\\.[0-9]{1,3}\\.[0-9]{1,3}\\.[0-9]{1,3}\\])|(([a-zA-Z\\-0-9]+\\.)+[a-zA-Z]{2,}))$";
           java.util.regex.Pattern p = java.util.regex.Pattern.compile(ePattern);
           java.util.regex.Matcher m = p.matcher(email);
           return m.matches();
}
