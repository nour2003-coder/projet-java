        TableModel model=members.getModel();
        cin.setText(model.getValueAt(rowNo, 0).toString());
        firstname.setText(model.getValueAt(rowNo, 1).toString());
        lastname.setText(model.getValueAt(rowNo, 2).toString());
        phonenumber.setText(model.getValueAt(rowNo, 3).toString());
        email.setText(model.getValueAt(rowNo, 4).toString());
