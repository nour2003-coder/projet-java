    public ArrayList<reader> getreadList() throws SQLException{
      ArrayList<reader>  readers =new ArrayList<reader>();
       Connection con = ConnectionProvider.getCon();
       String query ="select * from lecteur";
       Statement st;
       ResultSet rs;
       st=con.createStatement();
       rs=st.executeQuery(query);
       reader reader1;
       while(rs.next()){
           
           reader1=new reader(rs.getString("nom"),rs.getString("prenom"),Integer.parseInt(rs.getString("phonenumber")),rs.getString("cin"),rs.getString("email"),Integer.parseInt(rs.getString("status")),Integer.parseInt(rs.getString("book_count")));
           readers.add(reader1);
       }
       return(readers);
       
    }  
        public final void showread() {
    try {
        ArrayList<reader> readers = getreadList();
        String[] columnName = {"cin", "firstname", "lastname", "phonenumber", "email","status","book_count"};
        Object[][] rows = new Object[readers.size()][7];
        for (int i = 0; i < readers.size(); i++) {
            rows[i][0] = readers.get(i).getcin();
            rows[i][1] = readers.get(i).getfirstname();
            rows[i][2] = readers.get(i).getlastname();
            rows[i][3] =readers.get(i).getphone(); 
            rows[i][4] =readers.get(i).getemail();
            rows[i][6] =readers.get(i).getbook();
            if(readers.get(i).getstatues()==1){
                rows[i][5] ="has right to issue book";
                
            }
            else{
                rows[i][5] ="no right to issue book";
                
            }
               
            

        }
        
        tableread T = new tableread(rows, columnName);
        members.setModel(T);
        members.setRowHeight(60);
        members.getColumnModel().getColumn(4).setPreferredWidth(100);
    } catch (SQLException ex) {
        Logger.getLogger(managelecteurs.class.getName()).log(Level.SEVERE, null, ex);
    }
}

        
